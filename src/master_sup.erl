%% --------------------------------------------------------------------
init([]) ->
    Workers=children(),
  %  EventMgr=[{master_log,{master_log,start,[]},
%	      permanent,2000, worker,[master_log]}],
    EventMgr=[],
    AllChildren=lists:append(EventMgr,Workers),
   
    {ok,{{one_for_one,5,10}, 
	AllChildren
	}
    }.
children()->
    [
     ?CHILD(common,worker),
     ?CHILD(dbase,worker),
     ?CHILD(master,worker),
     ?CHILD(control,worker)
    ].
